package faculdade.br.picuma.model;

public class MensagemSucesso {
	
	private int idSucesso;
	private int codigoSucesso;
	private String mensagemSucesso;
	private String perteceA;
	private String quemDispara;
	
	public int getIdSucesso() {
		return idSucesso;
	}
	public void setIdSucesso(int idSucesso) {
		this.idSucesso = idSucesso;
	}
	public int getCodigoSucesso() {
		return codigoSucesso;
	}
	public void setCodigoSucesso(int codigoSucesso) {
		this.codigoSucesso = codigoSucesso;
	}
	public String getMensagemSucesso() {
		return mensagemSucesso;
	}
	public void setMensagemSucesso(String mensagemSucesso) {
		this.mensagemSucesso = mensagemSucesso;
	}
	public String getPerteceA() {
		return perteceA;
	}
	public void setPerteceA(String perteceA) {
		this.perteceA = perteceA;
	}
	public String getQuemDispara() {
		return quemDispara;
	}
	public void setQuemDispara(String quemDispara) {
		this.quemDispara = quemDispara;
	}

}
