package faculdade.br.picuma.model;

public class MensagemErro {
	
	private int idErro;
	private int codigoErro;
	private String mensagemErro;
	private String perteceA;
	private String quemDispara;
	
	public int getIdErro() {
		return idErro;
	}
	public void setIdErro(int idErro) {
		this.idErro = idErro;
	}
	public int getCodigoErro() {
		return codigoErro;
	}
	public void setCodigoErro(int codigoErro) {
		this.codigoErro = codigoErro;
	}
	public String getMensagemErro() {
		return mensagemErro;
	}
	public void setMensagemErro(String mensagemErro) {
		this.mensagemErro = mensagemErro;
	}
	public String getPerteceA() {
		return perteceA;
	}
	public void setPerteceA(String perteceA) {
		this.perteceA = perteceA;
	}
	public String getQuemDispara() {
		return quemDispara;
	}
	public void setQuemDispara(String quemDispara) {
		this.quemDispara = quemDispara;
	}
	

}
